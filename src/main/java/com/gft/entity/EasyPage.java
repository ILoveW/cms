package com.gft.entity;

import java.util.List;

public class EasyPage {
	/**
	 * easyuitree实体类
	 * 
	 * @author shixi
	 *
	 */
	private List rows;
	private Long total;

	public Long getTotal() {
		return total;
	}

	public void setTotal(Long total) {
		this.total = total;
	}

	public List getRows() {
		return rows;
	}

	public void setRows(List rows) {
		this.rows = rows;
	}

	public EasyPage(Long total, List rows) {

		this.total = total;
		this.rows = rows;
	}
}
